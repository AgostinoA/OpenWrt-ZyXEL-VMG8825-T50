#include <common.h>

/* First function in initialize sequence */
int cpu_init(void)
{
	disable_interrupts();
	return 0;
}

/* Last function before Linux start */
int cleanup_before_linux(void)
{
	dcache_disable();
	icache_disable();
	return 0;
}

